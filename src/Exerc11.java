
public class Exerc11 {

	public static void main(String[] args) {
		int valor = 10;
		System.out.println(valor);
		valor++;
		System.out.println(valor);
		valor--;
		System.out.println(valor);
		
		valor = 100;
		System.out.println(valor);
		++valor;
		System.out.println(valor);
		--valor;
		System.out.println(valor);
		}

}
